package ch02;

public class VarEx1 {

    public static void main(String[] args) {
        int year = 0;
        int age = 28;

        System.out.println(year);
        System.out.println(age);

        year = age + 2020;
        age = age + 1;

        System.out.println(year);
        System.out.println(age);
    }
}